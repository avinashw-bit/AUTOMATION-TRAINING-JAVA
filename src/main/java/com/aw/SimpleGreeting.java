package com.aw;

public class SimpleGreeting {

    // Method to return greeting message
    public static String greet(String name) {
        return "Hello " + name;
    }

    public static void main(String[] args) {
        // Testing the greet method
        System.out.println(greet("Joseph"));
        System.out.println(greet("Swapna"));
        System.out.println(greet("Ammy"));
    
    }
}


