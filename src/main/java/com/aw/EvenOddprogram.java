package com.aw;

public class EvenOddprogram {
    public static void main(String[] args) {
        int number = 9; 
        // change this value to test other numbers
        if (number % 2 == 0) {
            System.out.println(number + " is even");
        } else {
            System.out.println(number + " is odd");
        }
    }
}
